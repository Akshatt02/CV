# Download and prepare dataset
print("\nDownloading Cats vs Dogs dataset...")

# Create dataset directory
os.makedirs('dataset', exist_ok=True)

# Download a small subset of cats and dogs dataset (using TensorFlow dataset)
import tensorflow_datasets as tfds

# Load cats vs dogs dataset from tensorflow datasets
print("Loading dataset from TensorFlow Datasets...")
(ds_train, ds_test), ds_info = tfds.load(
    'cats_vs_dogs',
    split=['train[:80%]', 'train[80%:90%]'],  # Use 80% for training, 10% for testing
    shuffle_files=True,
    as_supervised=True,
    with_info=True
)

print(f"Dataset info: {ds_info}")
print(f"Number of training samples: {tf.data.experimental.cardinality(ds_train).numpy()}")
print(f"Number of test samples: {tf.data.experimental.cardinality(ds_test).numpy()}")

# Preprocessing function
def preprocess_image(image, label):
    # Resize images to 64x64
    image = tf.image.resize(image, (64, 64))
    # Normalize pixel values to [0,1]
    image = tf.cast(image, tf.float32) / 255.0
    return image, label

# Prepare the datasets
batch_size = 32
train_dataset = ds_train.map(preprocess_image).batch(batch_size).prefetch(tf.data.AUTOTUNE)
test_dataset = ds_test.map(preprocess_image).batch(batch_size).prefetch(tf.data.AUTOTUNE)

print("\n✓ Dataset loaded and preprocessed successfully!")

# Building the CNN
print("\nBuilding Basic CNN Model...")

basic_cnn = tf.keras.models.Sequential([
    # Convolution + Pooling layers
    tf.keras.layers.Conv2D(filters=32, kernel_size=3, activation='relu', input_shape=[64, 64, 3]),
    tf.keras.layers.MaxPool2D(pool_size=2, strides=2),

    # Second convolutional layer
    tf.keras.layers.Conv2D(filters=64, kernel_size=3, activation='relu'),
    tf.keras.layers.MaxPool2D(pool_size=2, strides=2),

    # Third convolutional layer
    tf.keras.layers.Conv2D(filters=128, kernel_size=3, activation='relu'),
    tf.keras.layers.MaxPool2D(pool_size=2, strides=2),

    # Flattening
    tf.keras.layers.Flatten(),

    # Full Connection
    tf.keras.layers.Dense(units=256, activation='relu'),
    tf.keras.layers.Dropout(0.5),

    # Output Layer
    tf.keras.layers.Dense(units=1, activation='sigmoid')
])

# Compile the model
basic_cnn.compile(optimizer='adam',
                  loss='binary_crossentropy',
                  metrics=['accuracy'])

print("\nBasic CNN Model Summary:")
basic_cnn.summary()

# Train the model (with fewer epochs for demonstration)
print("\nTraining Basic CNN Model (5 epochs for demonstration)...")
history = basic_cnn.fit(
    train_dataset,
    validation_data=test_dataset,
    epochs=1,
    verbose=1
)

print("\n✓ Task 1 Complete: Basic CNN trained successfully!")
print(f"Final Training Accuracy: {history.history['accuracy'][-1]:.4f}")
print(f"Final Validation Accuracy: {history.history['val_accuracy'][-1]:.4f}")

