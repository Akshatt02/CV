# Custom Dataset class for Cats vs Dogs
class CatsDogsDataset(Dataset):
    def __init__(self, tf_dataset, transform=None):
        self.tf_dataset = list(tf_dataset)
        self.transform = transform

    def __len__(self):
        return len(self.tf_dataset)

    def __getitem__(self, idx):
        image, label = self.tf_dataset[idx]
        # Convert tensorflow tensor to numpy and then to PIL
        image_np = (image.numpy() * 255).astype(np.uint8)
        image_pil = Image.fromarray(image_np)

        if self.transform:
            image_pil = self.transform(image_pil)

        # Fix: Convert TensorFlow scalar tensor to a Python scalar before creating PyTorch tensor
        return image_pil, torch.tensor(label.numpy(), dtype=torch.float32)

# Define transforms for PyTorch
transform = transforms.Compose([
    transforms.Resize((64, 64)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
])

# Create PyTorch datasets
print("Converting dataset to PyTorch format...")

# Get a small subset for demonstration (first 1000 training and 200 test samples)
train_subset = list(train_dataset.take(32))  # Take 32 batches
test_subset = list(test_dataset.take(10))     # Take 10 batches

# Flatten batches
train_samples = []
for batch_images, batch_labels in train_subset:
    for img, lbl in zip(batch_images, batch_labels):
        train_samples.append((img, lbl))
        if len(train_samples) >= 500:  # Limit to 500 samples for demonstration
            break
    if len(train_samples) >= 500:
        break

test_samples = []
for batch_images, batch_labels in test_subset:
    for img, lbl in zip(batch_images, batch_labels):
        test_samples.append((img, lbl))
        if len(test_samples) >= 100:  # Limit to 100 samples for demonstration
            break
    if len(test_samples) >= 100:
        break

# Create datasets
train_dataset_pytorch = CatsDogsDataset(train_samples, transform=transform)
test_dataset_pytorch = CatsDogsDataset(test_samples, transform=transform)

train_loader = DataLoader(train_dataset_pytorch, batch_size=32, shuffle=True)
test_loader = DataLoader(test_dataset_pytorch, batch_size=32, shuffle=False)

print(f"PyTorch dataset created with {len(train_dataset_pytorch)} training and {len(test_dataset_pytorch)} test samples")

# Define the CNN class
class CatDogCNN(nn.Module):
    def __init__(self):
        super(CatDogCNN, self).__init__()
        # Convolutional layers
        self.conv_layers = nn.Sequential(
            # First conv block
            nn.Conv2d(3, 32, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            # Second conv block
            nn.Conv2d(32, 64, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            # Third conv block
            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),
        )

        # Calculate the size after convolutions (for 64x64 input)
        # After 3 MaxPool2d(2,2): 64 -> 32 -> 16 -> 8
        self.fc_input_size = 128 * 8 * 8

        # Fully connected layers
        self.fc_layers = nn.Sequential(
            nn.Flatten(),
            nn.Linear(self.fc_input_size, 256),
            nn.ReLU(),
            nn.Dropout(0.5),
            nn.Linear(256, 1),
            nn.Sigmoid()
        )

    def forward(self, x):
        x = self.conv_layers(x)
        x = self.fc_layers(x)
        return x

# Create model instance
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
pytorch_cnn = CatDogCNN().to(device)
print(f"\nPyTorch CNN Architecture:")
print(f"Model created on device: {device}")

# Define loss function and optimizer
criterion = nn.BCELoss()
optimizer = optim.Adam(pytorch_cnn.parameters(), lr=0.001)

print("\nTraining PyTorch CNN (3 epochs for demonstration)...")

# Training loop
train_losses = []
test_accuracies = []

for epoch in range(1):  # 3 epochs for demonstration
    # Training phase
    pytorch_cnn.train()
    running_loss = 0.0
    for images, labels in train_loader:
        images, labels = images.to(device), labels.to(device)

        # Forward pass
        outputs = pytorch_cnn(images)
        loss = criterion(outputs, labels.view(-1, 1))

        # Backward pass and optimize
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

        running_loss += loss.item()

    avg_loss = running_loss / len(train_loader)
    train_losses.append(avg_loss)

    # Validation phase
    pytorch_cnn.eval()
    correct = 0
    total = 0
    with torch.no_grad():
        for images, labels in test_loader:
            images, labels = images.to(device), labels.to(device)
            outputs = pytorch_cnn(images)
            predicted = (outputs > 0.5).float()
            total += labels.size(0)
            correct += (predicted.view(-1) == labels).sum().item()

    accuracy = 100 * correct / total
    test_accuracies.append(accuracy)

    print(f'Epoch [{epoch+1}/3], Loss: {avg_loss:.4f}, Validation Accuracy: {accuracy:.2f}%')

print("\n✓ Task 3 Complete: PyTorch CNN trained successfully!")

