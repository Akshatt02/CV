def contraharmonic_mean(img, k=3, Q=1.5):
  img = img.astype(np.float32)
  kernel = np.ones((k,k))
  num = cv2.filter2D(img**(Q+1), -1, kernel)
  den = cv2.filter2D(img**Q + 1e-6, -1, kernel)
  return (num / den).astype(np.uint8)
