def detect_noise_type(image):
    hist = cv2.calcHist([image], [0], None, [256], [0, 256])
    total_pixels = image.size
    zero_pixels = hist[0][0]
    max_pixels = hist[255][0]
    zero_percentage = zero_pixels / total_pixels
    max_percentage = max_pixels / total_pixels
    if zero_percentage > 0.02 and max_percentage > 0.02:
        return 'salt_pepper'
    elif zero_percentage > 0.02:
        return 'pepper'
    elif max_percentage > 0.02:
        return 'salt'
    else:
        return 'gaussian'

def apply_automatic_filter(image):
    noise_type = detect_noise_type(image)
    ksize = 5
    filtered_image = image.copy()
    filter_name = ""

    if noise_type == 'salt_pepper':
        filtered_image = cv2.medianBlur(image, ksize)
        filter_name = "Median Filter (Salt & Pepper)"
    elif noise_type == 'salt':
        filtered_image = contraharmonic_mean(image, k=ksize, Q=1.5)
        filter_name = "Contraharmonic Mean Filter (Salt)"
    elif noise_type == 'pepper':
        filtered_image = contraharmonic_mean(image, k=ksize, Q=-1.5)
        filter_name = "Contraharmonic Mean Filter (Pepper)"
    else:
        filtered_image = cv2.blur(image, (ksize, ksize))
        filter_name = "Arithmetic Mean Filter (Gaussian)"

    return filtered_image, filter_name

