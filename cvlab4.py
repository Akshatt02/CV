def geometric_mean_filter(img, k=3):
  img = img.astype(np.float32) + 1
  kernel = np.ones((k,k))
  log_img = np.log(img)
  filtered = cv2.filter2D(log_img, -1, kernel)
  return np.exp(filtered / (k*k)).astype(np.uint8)
