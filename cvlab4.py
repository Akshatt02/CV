def harmonic_mean_filter(img, k=3):
  img = img.astype(np.float32)
  kernel = np.ones((k,k))
  denom = cv2.filter2D(1.0 / (img + 1e-6), -1, kernel)
  return ((k*k) / denom).astype(np.uint8)
salt = random_noise(img, mode='salt', amount=0.1)
